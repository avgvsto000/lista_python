# Média de preços
preco1 = float(input("Digite o preço do produto 1: "))
preco2 = float(input("Digite o preço do produto 2: "))
preco3 = float(input("Digite o preço do produto 3: "))
preco4 = float(input("Digite o preço do produto 4: "))
media_preco = (preco1 + preco2 + preco3 + preco4) / 4
print(f"A média dos preços é: R${media_preco:.2f}")